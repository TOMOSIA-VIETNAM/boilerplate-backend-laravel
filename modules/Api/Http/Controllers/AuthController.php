<?php

namespace Modules\Api\Http\Controllers;

use App\Containers\User\Models\User;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\Hash;
use Illuminate\Validation\ValidationException;

/**
 * Authentication Controller for API Module
 * 
 * Handles authentication operations via REST API endpoints.
 * Provides login/logout functionality with token-based authentication.
 */
class AuthController extends Controller
{
    /**
     * Handle user login and token generation
     * 
     * Authenticates user credentials and generates a new API token
     * for subsequent authenticated requests.
     *
     * @param Request $request
     * @return \Illuminate\Http\JsonResponse
     * @throws ValidationException
     */
    public function login(Request $request)
    {
        $request->validate([
            'email' => 'required|email',
            'password' => 'required',
        ]);

        $user = User::where('email', $request->email)->first();

        if (!$user || !Hash::check($request->password, $user->password)) {
            throw ValidationException::withMessages([
                'email' => ['The provided credentials are incorrect.'],
            ]);
        }

        $token = $user->createToken('api-token')->plainTextToken;

        return response()->json([
            'token' => $token,
            'user' => $user
        ]);
    }

    /**
     * Handle user logout and token revocation
     * 
     * Revokes the current user's API token to invalidate
     * subsequent authenticated requests.
     *
     * @param Request $request
     * @return \Illuminate\Http\JsonResponse
     */
    public function logout(Request $request)
    {
        $request->user()->currentAccessToken()->delete();

        return response()->json(['message' => 'Logged out successfully']);
    }
} 